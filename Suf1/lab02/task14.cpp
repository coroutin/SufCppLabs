#include <iostream>

int main() {
    int k; // Число ступеней которое вводит пользователь
    std::string end; //
    std::cin >> k;
    if (k % 100 >= 11 && k % 100 <= 14) {end = "ступеней";}
    else if (k % 10 == 1) {end = "ступень";}
    else if (k % 10 >= 2  && k % 10 <= 4) {end = "ступени";}
    else {end = "ступеней";}
    std::cout << "Сударыня, сегодня мне посчастливилось одолеть " << k << " " << end << " в занимательной дивертисции.";
    return 0;
}
