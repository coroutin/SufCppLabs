int lesinke(int n, int last) {
    if (n == 0) return 1;

    int ways = 0;

    int max = (last < n + 1 ? last - 1 : n);

    for (int k = 1; k <= max; k++) {
        ways += lesinke(n - k, k);
    }

    return ways;
}

