#include <iostream>

void min_odd_digit(long long n, int &mn, int &pos_mn) {
    mn = -1;
    pos_mn = -1;
    n = llabs(n);

    long long t = n; // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω–æ–≥–æ —Ö—Ä–∞–Ω–µ–Ω–∏—è
    int len = 0; // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª–∏–Ω—ã —á–∏—Å–ª–∞
    while (t > 0) {
        len++;
        t /= 10;
    }

    int pos_from_right = 1; // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –ø–æ–∑–∏—Ü–∏–∏ —Å–ø—Ä–∞–≤–∞
    while (n > 0) {
        int d = n % 10; // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è —Ü–∏—Ñ—Ä—ã —á–∏—Å–ª–∞

        if (d % 2 == 1) { // –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –Ω–µ—á—ë—Ç–Ω–æ—Å—Ç—å
            int left_pos = len - pos_from_right + 1; // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –ø–æ–∑–∏—Ü–∏–∏ —Å–ª–µ–≤–∞
            // –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –º–∞–Ω–∏–º–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ (–º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π –µ—â—ë –Ω–µ—Ç –∏–ª–∏ —Ü–∏—Ñ—Ä–∞ –º–µ–Ω—å—à–µ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π –∏–ª–∏ —Ü–∏—Ñ—Ä–∞ —Ä–∞–≤–Ω–∞ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–π, –Ω–æ —Ä–∞–∑—Ä—è–¥ —Å—Ç–∞—Ä—à–µ)
            if (mn == -1 || d < mn || (d == mn && left_pos < pos_mn)) {
                mn = d;
                pos_mn = left_pos;
            }
        }
        n /= 10;
        pos_from_right++;
    }
}

int main() {
    long long n; // –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —á–∏—Å–ª–∞ –∫–æ—Ç–æ—Ä–æ–µ –≤–≤–µ–¥—ë—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
    int mn, pos_mn = 0;

    std::cout << "–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ: ";
    std::cin >> n;

    min_odd_digit(n, mn, pos_mn);

    if (pos_mn != -1) {
        std::cout <<"–ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –Ω–µ—á—ë—Ç–Ω–∞—è —Ü–∏—Ñ—Ä–∞ —á–∏—Å–ª–∞: " << mn << " ü•∏, " << "–ï—ë –ø–æ–∑–∏—Ü–∏—è –≤ —á–∏—Å–ª–µ: " << pos_mn << " ü§©." << std::endl;
    }
    else std::cout << "–ù–∞–∏–º–µ–Ω—å—à–µ–π –Ω–µ—á—ë—Ç–Ω–æ–π —Ü–∏—Ñ—Ä—ã –Ω–µ –Ω–∞—à–ª–æ—Å—åüò≠";
    return 0;
}